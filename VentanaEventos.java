    public void mostrarEventos() {
        String line = "";
        String cvsSplitBy = ",";
        try (BufferedReader br = new BufferedReader(new FileReader("C:\\Users\\joaqu\\OneDrive\\Documentos\\NetBeansProjects\\menu nvo\\menu nvo\\src\\main\\java\\eventos.csv"))) {
            br.readLine();
            StringBuilder sb = new StringBuilder();
            while ((line = br.readLine()) != null) {
                String[] event = line.split(cvsSplitBy);
                if (event.length < 3) {
                    continue;
                }
                sb.append("ID: ").append(event[0])
                        .append("\tNombre: ").append(event[1])
                        .append("\tUbicaciÃ³n: ").append(event[2])
                        .append("\n");
            }
            jTextArea1.setText(sb.toString());
        } catch (IOException e) {
            e.printStackTrace();
        }
    }


    
