    private void mostrarArchivoEnTextArea(String rutaArchivo, javax.swing.JTextArea textArea) {
        StringBuilder sb = new StringBuilder();
        String linea;

        try (BufferedReader br = new BufferedReader(new FileReader(rutaArchivo))) {
            br.readLine();
            while ((linea = br.readLine()) != null) {
                String[] campos = linea.split(",");
                if (campos.length < 5) {
                    continue;
                }
                String formattedLine = String.format("ID: %-10s Nombre: %-20s Ubicación: %-20s Entradas: %-10s Precio de Entrada: %-10s%n", 
                        campos[0], campos[1], campos[2], campos[3], campos[4]);
                sb.append(formattedLine);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        textArea.setText(sb.toString());
    }



