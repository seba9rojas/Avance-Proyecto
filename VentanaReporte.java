    private void mostrarArchivoEnTextAreaUsuarios(String rutaArchivo, javax.swing.JTextArea textArea) {
        StringBuilder sb = new StringBuilder();
        String linea;

        try (BufferedReader br = new BufferedReader(new FileReader(rutaArchivo))) {
            br.readLine();
            while ((linea = br.readLine()) != null) {
                if (linea.trim().isEmpty()) {
                    continue; // Si la línea está vacía, ignórala
                }
                String[] campos = linea.split(",");
                sb.append("RUT: ").append(campos[0])
                        .append("\tContraseña: ").append(campos[1])
                        .append("\n");
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        textArea.setText(sb.toString());
    }


